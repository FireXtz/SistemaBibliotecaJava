    private void jbDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarActionPerformed
        int linha = jTable1.getSelectedRow(); 
        
        if (linha < 0){
        JOptionPane.showMessageDialog(this, "USUÁRIO NÃO SELECIONADO!");
        
        }
        
        else {
          int codigo = (int) jTable1.getValueAt(linha, 0);
          controllerEmprestimo.excluirEmprestimoController(codigo);
          carregarEmprestimos();
          LimparCampos();
          JOptionPane.showMessageDialog(this, "USUÁRIO EXCLUIDO COM EXITO!");
        }
        
        
        
    }//GEN-LAST:event_jbDeletarActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        modelEmprestimo = new ModelEmprestimo();
        cadAlt = "alt";
        
         int linha = jTable1.getSelectedRow(); 
      
        
        if (linha < 0){
        JOptionPane.showMessageDialog(this, "LIVRO NÃO SELECIONADO!");
        
        }
        
        else {
           
          int codigo = (int) jTable1.getValueAt(linha, 0);
          
          controllerEmprestimo.alterarEmprestimoController(codigo);
          jtfId.setText(String.valueOf(modelEmprestimo.getId_emprestimo()));
          jtfLivro.setText(modelEmprestimo.getLivro_alugado());
          jtfAluno.setText(String.valueOf(modelEmprestimo.getUsu_emprestimo()));
          jFormattedEmprestimodata.setText(modelEmprestimo.getData_emprestimo());
          jFormattedDataPrevisao.setText(String.valueOf(modelEmprestimo.getData_previsao()));
          jFormattedDevolucaoData.setText(modelEmprestimo.getData_devolucao());
          
          
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jbAlterarActionPerformed

