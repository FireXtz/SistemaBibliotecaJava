    private void jbConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbConfirmarActionPerformed
     modelEmprestimo = new ModelEmprestimo();
     modelEmprestimo.setLivro_alugado(jtfLivro.getText());
     modelEmprestimo.setUsu_emprestimo(jtfAluno.getText());
     modelEmprestimo.setData_emprestimo(jFormattedEmprestimodata.getText());
     modelEmprestimo.setData_previsao(jFormattedDataPrevisao.getText());
     modelEmprestimo.setData_devolucao(jFormattedDevolucaoData.getText());
        
        if (cadAlt.equals("cad")) {
            
            
        if (controllerEmprestimo.salvarLivroController(modelEmprestimo)){
            
        JOptionPane.showMessageDialog(this,"O EMPRESTIMO FOI UM SUCESSO!","!",JOptionPane.INFORMATION_MESSAGE);
           LimparCampos();
           carregarEmprestimos();
       }
      
       else {
        JOptionPane.showMessageDialog(this, "ERRO AO SOLICITAR O EMPRESTIMO","!",JOptionPane.ERROR_MESSAGE);
       }
            
            
            
        } else{
            modelEmprestimo.setId_emprestimo(Integer.parseInt(jtfId.getText()));
             if (controllerEmprestimo.atualizarEmprestimoController(modelEmprestimo)){
                  
                 
               
                 
        JOptionPane.showMessageDialog(this,"LIVRO ALTERADO COM SUCESSO!","!",JOptionPane.INFORMATION_MESSAGE);
           
           LimparCampos();
           carregarEmprestimos();
       }
      
       else {
        JOptionPane.showMessageDialog(this, "ERRO AO ALTERAR O LIVRO","!",JOptionPane.ERROR_MESSAGE);
       }
        
        }
  
    

        
        
        
    }//GEN-LAST:event_jbConfirmarActionPerformed

    private void LimparCampos(){
    jtfAluno.setText("");
    jtfLivro.setText("");
    jtfId.setText("");
    jFormattedDataPrevisao.setText("");
    jFormattedDevolucaoData.setText("");
    jFormattedEmprestimodata.setText("");
  
    
    }
    
    
     private void carregarEmprestimos(){
           
     listaEmprestimo = controllerEmprestimo.getListaEmprestimoController();
     DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
     modelo.setNumRows(0);
     
     
         for (int i = 0; i < listaEmprestimo.size(); i++) {
             modelo.addRow(new Object[]{
             listaEmprestimo.get(i).getId_emprestimo(),
             listaEmprestimo.get(i).getLivro_alugado(),
             listaEmprestimo.get(i).getUsu_emprestimo(),
             listaEmprestimo.get(i).getData_emprestimo(),
             listaEmprestimo.get(i).getData_previsao(),
             listaEmprestimo.get(i).getData_emprestimo()
                
             
             });
         }
     
     
     }
    
    
    
